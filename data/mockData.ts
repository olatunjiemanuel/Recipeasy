/**
 * Mock AI recipe suggestions based on ingredients
 */
export const getRecipeSuggestions = (ingredients: string[]): Recipe[] => {
  // In a real app, this would call an API
  // For now, we'll return filtered mock recipes that contain at least one of the ingredients
  const lowercaseIngredients = ingredients.map(ing => ing.toLowerCase());
  
  return recipes.filter(recipe => 
    recipe.ingredients.some(ing => 
      lowercaseIngredients.some(userIng => 
        ing.name.toLowerCase().includes(userIng)
      )
    )
  );
};